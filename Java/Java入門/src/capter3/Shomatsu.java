package capter3;

public class Shomatsu {
	
	public static void main(String[] args) {
		System.out.println(12 + 3);
		System.out.println(12 - 3);
		System.out.println(12 * 3);
		System.out.println(12 / 3);
		System.out.println(12 % 3);
		
		System.out.println(5 / 2);
		System.out.println(5 % 2);
		
		System.out.println(2 + 3 * 4);
		System.out.println((2 + 3) * 4);
		
		System.out.println(1.2 + 0.3);
		System.out.println(1.2 - 0.3);
		System.out.println(1.2 * 0.3);
		System.out.println(1.2 / 0.3);
		System.out.println(1.2 % 0.3);
		
		System.out.println(Math.sqrt(2));
		System.out.println(Math.sqrt(3));
		System.out.println(Math.sqrt(5));
		
		System.out.println(Math.random());
		System.out.println(Math.random() * 6 + 1);
		
		System.out.println("total: " + (120 + 340 + 560) + "yen");
		System.out.println("合計: " + (120 + 340 + 560) + "円");
		
		System.out.println("\"JVA\" is \"Java Virtual Mchine\"");
	}

}
